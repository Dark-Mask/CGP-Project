main_menu = menu.Menu()
world = wld.World(world_data)
game_stat = stats.GameStat(len(world.collect_group))
player = pl.Player(100, world.height - 130, 50, 80)


run = True
game_start = False
while run:

    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            run = False

    if game_start:
        if screen.get_width() != world.width and screen.get_height() != world.height:
            os.environ['SDL_VIDEO_CENTERED'] = '1'
            screen = pygame.display.set_mode((world.width, world.height))

        #enemy collision
        if pygame.sprite.spritecollide(player, world.enemy_group, True):
            print('enemy hit')

        #bullet hit
        if pygame.sprite.spritecollide(player, world.bullet_group, True):
            game_stat.damage(30)

        #item collect
        if pygame.sprite.spritecollide(player, world.collect_group, True):
            game_stat.item_collected()


        world.update()
        player.update(world)
        game_stat.update()

        world.draw(screen)
        player.draw(screen)
        game_stat.draw(screen)

    else:
        main_menu.update()
        main_menu.draw(screen)

        if screen.get_width() != main_menu.width and screen.get_height() != main_menu.height:
            os.environ['SDL_VIDEO_CENTERED'] = '1'
            screen = pygame.display.set_mode((main_menu.width, main_menu.height))

        keys = pygame.key.get_pressed()
        if keys[pygame.K_RETURN]:
            if main_menu.get_selection() == 'start':
                game_start = True
            elif main_menu.get_selection()  == 'quit':
                run = False
        

    pygame.display.update()
    clock.tick(fps)
    
pygame.quit()